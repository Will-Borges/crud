    private void btnCadastrarFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarFuncActionPerformed

        // Pega os valores
        String nome = "'" + txtNome.getText() + "'";
        String sobrenome = "'" + txtSobrenome.getText() + "'";
        String cpf = "'" + txtCPF.getText() + "'";
        String nascimento = "'" + txtDataNasc.getText() + "'";
        String cargo = cbxCargo.getSelectedItem().toString();

        String idCargo = null;

        switch (cargo) {

            case "Estagiario":
                idCargo = "'1'";
                break;
            case "Auxiliar":
                idCargo = "2";
                break;
            case "Funcionario":
                idCargo = "3";
                break;
            case "Gerente":
                idCargo = "4";
                break;
        }
        

        if (nome.length() == 2 | sobrenome.length() == 2 | nascimento.length() == 4) {
            JOptionPane.showMessageDialog(rootPane, "Todos os campos devem ser preenchidos!", "Erro", JOptionPane.ERROR_MESSAGE, null);
        } else {
 
            //chama a conexÃ£o
            conexao connection = new conexao();

            String sql = "insert into funcionario (id, nome, sobrenome, cpf," + "\"dataNascimento" + "\", id_cargo)"
                    + "values (default," + nome + "," + sobrenome + "," + cpf + "," + nascimento + "," + idCargo + ")";

            int res = connection.executaSql(sql);
          
            if (res > 0) {
                JOptionPane.showMessageDialog(rootPane, "Cadastro realizado com sucesso!", "Mensagem", JOptionPane.DEFAULT_OPTION, null);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Erro durante o cadastro!", "Erro", JOptionPane.ERROR_MESSAGE, null);
            }
            connection.close();
        }
    }//GEN-LAST:event_btnCadastrarFuncActionPerformed

